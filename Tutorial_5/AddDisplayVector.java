import java.util.Vector;

public class AddDisplayVector {
    public static void main(String[] args) {
        
        Vector<String>fruits=new Vector<>();
        fruits.add("Apple");
        fruits.add("Orange");
        fruits.add("Banana");
        fruits.add("Guava");
        fruits.add("Grapes");
        for(String fruit:fruits){
            System.out.println(fruit);
        }
    }
}
